package InterfaceGrafica;

public class Aluno {

	private String nome;
	private int cpf;
	private int matricula;
	private String vertente;
	
	public Aluno(String nome, int cpf, int matricula, String vertente){
		
		this.nome = nome;
		this.cpf = cpf;
		this.matricula = matricula;
		this.vertente = vertente;
	}
	
}
