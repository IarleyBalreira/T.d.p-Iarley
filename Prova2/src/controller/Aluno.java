package controller;

public class Aluno {

	private int id;
	private int cordx, cordy;
	
public Aluno(int id, int cordx, int cordy) {

		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getCordx() {
		return cordx;
	}

	public void setCordx(int cordx) {
		this.cordx = cordx;
	}

	public int getCordy() {
		return cordy;
	}

	public void setCordy(int cordy) {
		this.cordy = cordy;
	}
}